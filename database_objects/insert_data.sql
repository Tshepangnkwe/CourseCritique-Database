-- 9. Update Course Statistics
MERGE INTO CourseStatistics cs
USING (
  SELECT 
    c.id AS course_id,
    COUNT(r.id) AS total_reviews,
    AVG(r.rating) AS average_rating,
    AVG(r.difficulty) AS average_difficulty,
    AVG(r.workload_hours) AS average_workload_hours,
    100 * AVG(r.would_take_again) AS would_take_again_pct
  FROM Course c
  LEFT JOIN Review r ON c.id = r.course_id
  WHERE r.status = 'approved'
  GROUP BY c.id
) stats
ON (cs.course_id = stats.course_id)
WHEN MATCHED THEN
  UPDATE SET 
    cs.total_reviews = stats.total_reviews,
    cs.average_rating = stats.average_rating,
    cs.average_difficulty = stats.average_difficulty,
    cs.average_workload_hours = stats.average_workload_hours,
    cs.would_take_again_pct = stats.would_take_again_pct,
    cs.last_updated = CURRENT_TIMESTAMP
WHEN NOT MATCHED THEN
  INSERT (course_id, total_reviews, average_rating, average_difficulty, average_workload_hours, would_take_again_pct, last_updated)
  VALUES (stats.course_id, stats.total_reviews, stats.average_rating, stats.average_difficulty, stats.average_workload_hours, stats.would_take_again_pct, CURRENT_TIMESTAMP);

COMMIT;

